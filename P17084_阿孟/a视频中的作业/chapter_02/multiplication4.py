#打印9x9乘法表
for i in range(1,10):
	for j in range(1,i+1):
		s=i*j
		if len(str(s))==1: #对齐
			print(j,'*',i,'=',s,end='  ')
		else:
			print(j,'*',i,'=',s,end=' ')
	print('') #换行

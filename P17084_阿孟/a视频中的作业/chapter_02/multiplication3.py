#上下左右互换打印9x9乘法表
for i in range(-9,0):
	print((9+i)*11*' ',end='')
	for j in range(1,-i+1):
		if -i>=j:
			print(j,'x',-i,'=',j*(-i),end='|')
	print('')
		
